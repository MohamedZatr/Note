package com.example.demo.view;

public class UserViewModel {
	
   private String id;
   private String name;
   private String password;
   private String email;
   private String type;
   private int numOfNotebooks;
   private int numOfNotes;
   
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public int getNumOfNotebooks() {
	return numOfNotebooks;
}
public void setNumOfNotebooks(int numOfNotebooks) {
	this.numOfNotebooks = numOfNotebooks;
}
public int getNumOfNotes() {
	return numOfNotes;
}
public void setNumOfNotes(int numOfNotes) {
	this.numOfNotes = numOfNotes;
}
   
	
		
}
